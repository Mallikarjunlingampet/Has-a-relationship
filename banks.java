package interfa;

public class banks {
	private String acctype;
	private String branchid;
	
	public banks(String acctype, String branchid) {
		
		this.acctype = acctype;
		this.branchid = branchid;
	}
	public String getAcctype() {
		return acctype;
	}
	public String getBranchid() {
		return branchid;
	}
	 
	

}
